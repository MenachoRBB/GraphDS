public class SameRechargeZone {
    private int id;
    private float cons;
    private String state;
    private int time;
    private float power;
    private String type;

    public SameRechargeZone(int id, float cons, String state, int time, float power, String type) {
        this.id = id;
        this.cons = cons;
        this.state = state;
        this.time = time;
        this.power = power;
        this.type = type;
    }


}
